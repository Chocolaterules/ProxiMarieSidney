package fr.formation.proxibanquemariesidney.metier;

/**
 * @author marie La classe "SavingsAccount" est la classe correspondant au
 *         compte épargne. Cette classe hérite de la classe mère "BankAccount".
 */
public class SavingsAccount extends BankAccount {

	/**
	 * Cette méthode permet d'instancier un compte épargne en précisant ses
	 * attributs introduits dans la classe mère.
	 * 
	 * @param accountNum  correspond au numéro de compte.
	 * @param balance     correspond au solde.
	 * @param openingDate correspond à la date d'ouverture.
	 */
	public SavingsAccount(int accountNum, int balance, String openingDate) {
		super(accountNum, balance, openingDate);
	}

	/**
	 * Cette méthode permet d'instancier un compte épargne sans préciser ses
	 * attributs.
	 */
	public SavingsAccount() {
	}
}
